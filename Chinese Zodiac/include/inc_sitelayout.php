Dynamic web content is parts of a website that are generated when the page is being loaded from various sources such as:

<ul>
  <li>Session Variables</li>
  <li>Database lookups</li>
  <li>Cookies</li>
</ul>

On the other hand, static web content is hard coded into HTML pages. Creating dynamic content by using a dynamic language leads to a much more flexible website.

Some dynamic languages that can be used to fetch and load content depending on varying criteria are:
<ul>
  <li>PHP</li>
  <li>JavaScript</li>
  <li>Ruby</li>
  <li>Lua</li>
  <li>Perl</li>  
</ul>